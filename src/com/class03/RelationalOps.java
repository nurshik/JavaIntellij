package com.class03;

public class RelationalOps {
	public static void main(String[] args) {
		
		int a=10;
		int b=7;
		
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a==b);
		System.out.println(a!=b);
		System.out.println("-----------");
		
		boolean r1=a>=b;
		boolean r2=a<=b;
		System.out.println(r1);
		System.out.println(r2);
		System.out.println("-----------");
		
		int c=20;
		int d=29;
		
		System.out.println(c==d);//checking equality
		System.out.println(c=d);//assigning the value d to c
	}

}
